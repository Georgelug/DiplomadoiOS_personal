// while
var counter = 1

while counter < 10 {
    print(counter)
    counter+=1
}

counter = 1
while 1...5 ~= counter{
    print(counter)
    counter+=1
}

counter = 0
repeat{
    print(counter)
    counter+=1
}while counter < 10
